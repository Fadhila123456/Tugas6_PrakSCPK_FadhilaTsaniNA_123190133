w=[0.15,0.20,0.30,0.30,0.05];% bobot untuk masing-masing kriteria

[m n]=size (x); %matriks m x n dengan ukuran sebanyak variabel x (input)
R=zeros (m,n); %membuat matriks R, yang merupakan matriks kosong
Y=zeros (m,n); %membuat matriks Y, yang merupakan titik kosong
for j=1:n,
    if k(j)==1, %statement untuk kriteria dengan atribut keuntungan
         R(:,j)=x(:,j)./max(x(:,j));
    else
        R(:,j)=min(x(:,j))./x(:,j);
    end;
end;

%tahapan kedua, proses perangkingan
for i=1:m,
    V(i)= sum(w.*R(i,:))
end;
resultMax = max(V);

if V(1) == resultMax
    set(handles.hasil, 'String', 'Membeli kendaraan untuk distribusi barang ke pabrik');
elseif V(2) == resultMax
    set(handles.hasil, 'String', 'Membeli tanah untuk membangun pabrik baru');
elseif V(3) == resultMax
    set(handles.hasil, 'String', 'Maintenance sarana teknologi informasi');
elseif V(4) == resultMax
    set(handles.hasil, 'String', 'Pengembangan produk baru');
end;

